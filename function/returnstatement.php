<?php declare(strict_types=1); // strict requirement
function addNumbers(float $a, float $b) : float { //menjalankan fungsi dengan nilai variabel yang bertipe float
    return $a + $b; //mengembarlikan nilai hasil penjumlahan dari kedua variabel
}
echo addNumbers(1.2, 5.2); // menampilkan hasil
?>
